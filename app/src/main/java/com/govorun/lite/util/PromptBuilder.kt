package com.govorun.lite.util

object PromptBuilder {

    fun build(mode: String): String = when (mode) {
        AiCleanerPrefs.MODE_LIGHT -> {
            "Исправь пунктуацию и очевидные опечатки. Не меняй смысл. Верни только исправленный текст без комментариев."
        }
        AiCleanerPrefs.MODE_CLEAN -> {
            "Сделай текст аккуратным и читаемым: исправь пунктуацию, явные ошибки, убери повторы и слова-паразиты, сохрани исходный смысл. Верни только исправленный текст без комментариев."
        }
        else -> {
            "Исправь пунктуацию и явные ошибки, убери лишние повторы и слова-паразиты. Сохрани смысл. Верни только исправленный текст без комментариев."
        }
    }
}
